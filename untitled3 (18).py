import numpy as np
from sklearn.datasets import load_iris
from sklearn.model_selection import train_test_split
from sklearn.neighbors import KNeighborsClassifier
from sklearn.metrics import accuracy_score
# Iris datasetini yuklash
iris = load_iris()
X = iris.data
y = iris.target
# Ma'lumotlarni o'qitish va sinov to'plamlariga bo'lish
X_train, X_test, y_train, y_test = train_test_split(X, y,
test_size=0.3, random_state=42)
# K-NN modelini yaratish va o'qitish
knn = KNeighborsClassifier(n_neighbors=3)
knn.fit(X_train, y_train)
# Sinov to'plamidagi natijalarni baholash
y_pred = knn.predict(X_test)
print(f"K-NN algoritmining aniqligi: {accuracy_score(y_test, y_pred)}")

