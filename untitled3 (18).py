from sklearn.metrics import accuracy_score

# Iris datasetini yuklash
iris = load_iris()
X = iris.data
y = iris.target

# Ma'lumotlarni o'qitish va sinov to'plamlariga bo'lish
X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.3, random_state=42
)

# Logistic Regression modelini yaratish va o'qitish
model = LogisticRegression(max_iter=200)
model.fit(X_train, y_train)

# Sinov to'plamida bashorat qilish
y_pred = model.predict(X_test)

# Aniqlikni tekshirish
print("Logistic Regression aniqligi:", accuracy_score(y_test, y_pred))

import numpy as np
from sklearn.datasets import load_iris
from sklearn.model_selection import train_test_split
from sklearn.svm import SVC
from sklearn.metrics import accuracy_score

# Iris datasetini yuklash
iris = load_iris()
X = iris.data
y = iris.target

# Ma'lumotlarni train va testga bo'lish
X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.3, random_state=42
)

# SVM modelini yaratish va o'qitish
svm_model = SVC(kernel='linear')
